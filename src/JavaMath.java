
// static import in java
import static java.lang.Math.*;

public class JavaMath {

	public static void main(String[] args) {

		System.out.println("Floor Value : " + floor(24.77));

		System.out.println("Ceil value : " + ceil(25.88));

		System.out.println("Max value is : " + max(200, 55));

		System.out.println("Min value is : " + min(100, 200));

		System.out.println("Round Value : " + round(25.35));

		System.out.println("Sqrt value : " + sqrt(16));

		System.out.println("Log value is : " + log(2));

		System.out.println("Todegree value is : " + toDegrees(12.35));

		System.out.println("Sin value is : " + sin(550));

		System.out.println("Cos value is : " + cos(45));

	}

}
